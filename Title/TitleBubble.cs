	//--------------------------------------------------------------------------------
	// 初期化 
	//--------------------------------------------------------------------------------
	public override void _Ready()
	{
		// ランダムにひとつ選ぶ 
		int rand = new Random().Next() % _sprites.Length;
		GD.Print(rand);
		for(int i=0; i<_sprites.Length; i++)
		{
			_sprites[i].Visible = (i==rand);
		}
	}

	//--------------------------------------------------------------------------------
	// 更新 
	//--------------------------------------------------------------------------------
	public override void _Process(double delta)
	{
		// 上に向かって移動 
	}
}
